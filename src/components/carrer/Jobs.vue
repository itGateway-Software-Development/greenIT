<script>
import { ref, watch } from "vue";
export default {
  props: ["job"],

  setup(props) {
    let bd = ref(null);
    let technicalSale = ref(null);
    let businessAnalyst = ref(null);
    let presale = ref(null);
    let graphic = ref(null);
    let showroom = ref(null);

    const scrollToSection = (section) => {
      let targetRef;

      if (section === "bd") {
        targetRef = bd;
      } else if (section === "technicalSale") {
        targetRef = technicalSale;
      } else if (section === "businessAnalyst") {
        targetRef = businessAnalyst;
      } else if (section === "presale") {
        targetRef = presale;
      } else if (section === "graphic") {
        targetRef = graphic;
      } else if (section === "showroom") {
        targetRef = showroom;
      }

      if (targetRef && targetRef.value) {
        const topOffset =
          targetRef.value.getBoundingClientRect().top + window.scrollY - 90;
        window.scrollTo({ top: topOffset, behavior: "smooth" });
      }
    };

    watch(
      () => props.job,
      (newjob) => {
        scrollToSection(newjob);
      }
    );

    return { bd, technicalSale, businessAnalyst, presale, graphic, showroom };
  },
};
</script>

